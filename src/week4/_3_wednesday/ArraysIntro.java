package week4._3_wednesday;

import java.util.Arrays;

public class ArraysIntro {

    public static void main(String[] args) {

        int [] arr = new int[5]; // An Integer array which has five elements
        arr[0] = 1;  // The first element of array is 1
        arr[1] = 2;
        arr[2] = 3;
        arr[3] = 4;
        arr[4] = 5; // The last element of array (index = arr.length-1)

        System.out.println(Arrays.toString(arr));

    }


}
