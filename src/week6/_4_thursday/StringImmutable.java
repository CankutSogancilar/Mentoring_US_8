package week6._4_thursday;

// STRING IMMUTABLE.

public class StringImmutable {

    public static void main(String[] args) {


        String str = "APPPLEGARDEN";
        str.toLowerCase();
        System.out.println(str);

        String str2 = "banana garden";

        String str3 = str2.toUpperCase();
        System.out.println("str2 = " + str2); // lower case banana garden
        System.out.println("str3 = " + str3); // UPPER CASE BANANA GARDEN





    }


}
