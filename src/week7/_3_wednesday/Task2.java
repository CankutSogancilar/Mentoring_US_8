// Create a function that can print hello world 50 times ==> helloWorld5Times()

public class Task2 {


    public static void main(String[] args) {

        helloWordFiftyTimes();


    }


    public static void helloWordFiftyTimes( ) {

        for (int i = 0; i < 50; i++) {   // That means fifty times
            System.out.println("Hello World");
        }
        // void is about return type of method


    }


    // 1) This method is a static method
    // 2) This is a void method
    // 3) This method is without parameter



}
