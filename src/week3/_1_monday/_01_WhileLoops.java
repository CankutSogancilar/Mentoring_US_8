package week3._1_monday;

public class _01_WhileLoops {
    public static void main(String[] args) {
        // Example 1: Write a program to print “Hello World” 5 times.

        // initialization expression
        int i = 1;

        // test expression
        while (i < 6) {
            System.out.println("Hello World");
            // update expression
            i++;
        }
    }
}
